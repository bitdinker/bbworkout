export function generateAiHint(exerciseName: string): string {
  const words = exerciseName.toLowerCase().split(' ');
  if (words.length === 1) return words[0];
  if (words.length >= 2) return `${words[0]} ${words[1]}`;
  return "gym exercise"; // Fallback
}
